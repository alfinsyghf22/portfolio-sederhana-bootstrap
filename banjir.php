<h3><b>CIREBON</b> - Hujan deras yang mengguyur selama lima jam mengakibatkan lima kecamatan di Kabupaten Cirebon Jawa Barat terendam banjir , Minggu dinihari (27/12/2020). Ketinggian air di beberapa titik bahkan mencapai 1,5 meter selain intensitas hujan yang tinggi banjir juga disebabkan pendangkalan dan meluapnya sungai setelah mendapatkan kiriman dari wilayah hulu Kabupaten Kuningan.<br>
<br>Ke lima kecamatan di Kabupaten Cirebon yang terendam banjir tersebut yakni Waled, Lemahabang, Astanajapura, Karangsembung dan Pangenan.
<br>
<br>Titik banjir terparah dialami Desa Mekarsari dan Desa Gunungsari di Kecamatan Waled dengan ketinggian air mencapai 1,5 meter.
Menurut Herman Warga Waled, Kabupaten Cirebon banjir langganan di Desa Gunungsari ini sudah dirasakan sejak Sabtu malam.
“Arus deras air dengan cepat masuk ke rumah-rumah warga setelah Sungai Ciberes yang mendapat kiriman air dari wilayah hulu meluap. Warga yang telah membendung pintu masuk dan lubang selokan tak bisa berbuat banyak setelah banjir rembes dan merendam rumah mereka,” kata Herman.
<br>
<br>Kepala BPBD Kabupaten CirebonAlex Suheriawan mengatakan, dari data sementara Badan Penanggulangan Bencana Daerah Kabupaten Cirebon banjir yang melanda Desa Gungusari ini merendam sedikitnya 350 rumah warga dengan ketinggian bervariasi dari 80-hingga 1,5 meter. <br>
<br>Sementara dari lima kecamatan yang terendam hampir 2.000 rumah yang terendam
“Sampai saat ini kondisi banjir masih merendam pemukiman warga pun khawatir banjir akan tinggi kembali jika hujan mengguyur dan kiriman air dari hulu tinggi,” kata Alex Suheriawan.
